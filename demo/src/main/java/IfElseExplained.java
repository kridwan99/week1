public class IfElseExplained {
    public static void main(String[] args) {

        int x = 10;
        if (x > 5) {
            System.out.println("x, is bigger than than 5");
        } else if (x > 10) {
            System.out.println("x, is bigger than than 10");

        } else {
            System.out.println("x is less than or equal to 5");
        }
    }
}
